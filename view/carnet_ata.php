				  	<h4>¿Qué es el Carnet ATA?</h4>
				    
				    <p>El Carnet ATA es un documento aduanero que permite la libre admisión temporal de mercancías, exenta de derechos de importación y otros impuestos, tales como el IVA en más de 80 países.</p>

				    <p>Resultado de convenios internacionales, el Carnet ATA ha sido utilizado internacionalmente por las empresas por más de 40 años, constituyendo un verdadero “pasaporte” para las mercancías en viajes de negocios o participación en ferias y eventos.</p>

				    <p>La Cámara de Comercio de Santiago (CCS) ha sido designada por el Servicio Nacional de Aduanas como la entidad emisora y garante del Carnet ATA en Chile. La CCS forma parte de una red mundial de entidades ATA, liderada por la Cámara de Comercio Internacional (ICC).</p>

