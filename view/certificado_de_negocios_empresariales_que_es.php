				  	<h4>¿Qué es?</h4>
				    
				    <p>Este documento, emitido por la Cámara de Comercio de Santiago y Ernst & Young, con el apoyo de ProChile, es una verdadera “carta de presentación” de las empresas chilenas que incursionan en mercados internacionales, constituyendo un elemento generador de confianza y un instrumento diferenciador en la toma de decisiones de negocios.</p>

				    <h4>El documento incluye:</h4>

				    <p>Identificación de la empresa.<br>
				    Antecedentes legales, morosidad comercial, financieros, de promoción de exportaciones, certificaciones.<br>
				    Operaciones de comercio exterior.<br>
				    Este certificado se elabora combinando información comercial-financiera y legal, disponible a nivel nacional, con información proporcionada por las mismas empresas y verificada por las entidades emisoras del certificado, especialistas en el manejo de este tipo de datos.</p>

