				  	<h4>¿Qué es la Tarjeta APEC?</h4>
				    
				    <p>La tarjeta de viaje de negocios APEC (ABTC), te permitirá circular por 18 países sin necesidad de visa, obteniendo un trato preferencial en los trámites de migración. Este documento está enfocado a ejecutivos y permite a su titular una entrada más expedita a los países de la región del Asia- Pacífico. Los períodos máximos de estadía  son de 90 días.</p>

				    <p>En Estados Unidos Y Canadá se recibe trato preferencial, en filas de migración en aeropuertos internacionales.</p>

				    <p>La Tarjeta APEC tiene una validez equivalente a la vigencia del pasaporte, con un máximo de tres años desde la fecha de su emisión.</p>

